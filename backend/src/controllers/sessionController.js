import { chatClient, streamClient } from "../lib/stream.js";
import Session from "../models/Session.js";


export async function createSession(req, res) {
    try {
        const { problem, difficulty } = req.body;
        const userId = req.user._id;
        const clerkId = req.user.clerkId;

        if (!problem || !difficulty) {
            return res.status(400).json({ message: "Problem and Difficulty are required" });
        }

        // Generate unique call id
        const callId = `session_${Date.now()}_${Math.random().toString(36).substring(7)}`;

        // Create session in DB
        const session = await Session.create({ problem, difficulty, host: userId, callId });

        try {
            // Create video call
            await streamClient.video.call("default", callId).getOrCreate({
                data: {
                    created_by_id: clerkId,
                    custom: { problem, difficulty, sessionId: session._id.toString() },
                },
            });

            const channel = chatClient.channel("messaging", callId, {
                name: `${problem} Session`,
                created_by_id: clerkId,
                members: [clerkId]
            })

            await channel.create();

        } catch (error) {
            await Session.findByIdAndDelete(session._id);
            console.error("Error creating Stream resources: ", error);
            return res.status(500).json({ message: "Failed to create session resources" })
        }

        res.status(201).json({ session })
    } catch (error) {
        console.error("Error in createSession controller: ", error)
        res.status(500).json({ message: "Internal Server Error: createSession controller" })
    }
};

export async function getActiveSessions(_, res) {
    try {
        const sessions = await Session.find({ status: "active" })
            .populate("host", "name profileImage email clerkId")
            .sort({ createdAt: -1 })
            .limit(20)

        res.status(200).json({ sessions });
    } catch (error) {
        console.error("Error in getActiveSessions controller: ", error);
        res.status(500).json({ message: "Internal Server Error: getActiveSessions controller" })
    }
};

export async function getMyRecentSessions(req, res) {
    try {
        const userId = req.user._id;

        const sessions = await Session.find({
            status: "completed",
            $or: [{ host: userId }, { participant: userId }],
        })
            .sort({ createdAt: -1 })
            .limit(10);
        res.status(200).json({ sessions });
    } catch (error) {
        console.error("Error in getMyRecentSessions controller: ", error);
        res.status(500).json({ message: "Internal Server Error: getMyRecentSessions controller" })
    }
};

export async function getSessionById(req, res) {
    try {
        const { id } = req.params;

        const session = await Session.findById(id)
            .populate("host", "name email profileImage clerkId")
            .populate("participant", "name email profileImage clerkId")

        if (!session) {
            return res.status(404).json({ message: "Session not found!" })
        }

        res.status(200).json({ session })
    } catch (error) {
        console.error("Error in getSessionById controller: ", error);
        res.status(500).json({ message: "Internal Server Error: getSessionById controller" })
    }
};

export async function joinSession(req, res) {
    try {
        const { id } = req.params;
        const userId = req.user._id;
        const clerkId = req.user.clerkId;

        const session = await Session.findById(id);

        if (!session) {
            return res.status(404).json({ message: "Session not found!" })
        }

        if (session.status !== "active") {
            return res.status(400).json({ message: "Cannot join a completed session" })
        }

        if (session.host.toString() === userId.toString()) {
            return res.status(400).json({ message: "Host cannot join their own session as participant" })
        }

        // Check if session is already full
        if (session.participant) {
            return res.status(409).json({ message: "Session is Full" });
        };


        session.participant = userId;
        await session.save();

        const channel = chatClient.channel("messaging", session.callId)

        await channel.addMembers([clerkId]);

        res.status(200).json({ session });

    } catch (error) {
        console.error("Error in joinSession controller: ", error);
        res.status(500).json({ message: "Internal Server Error: joinSession controller" })
    }
};

export async function endSession(req, res) {
    try {
        const { id } = req.params;
        const userId = req.user._id;

        const session = await Session.findById(id);

        if (!session) {
            return res.status(404).json({ message: "Session not found" });
        }

        if (session.host.toString() !== userId.toString()) {
            return res.status(403).json({ message: "Only Host can end the session" });
        }

        if (session.status === "completed") {
            return res.status(400).json({ message: "Session is already completed" });
        }


        // Delete Video call
        const call = streamClient.video.call("default", session.callId);
        await call.delete({ hard: true });

        // Delete stream chat channel
        const channel = chatClient.channel("messaging", session.callId);
        await channel.delete()

        session.status = "completed";
        await session.save();

        res.status(200).json({ message: "Session ended successfully" });
    } catch (error) {
        console.error("Error in endSession controller: ", error);
        res.status(500).json({ message: "Internal Server Error: endSession controller" })
    }
};

